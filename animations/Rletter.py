import turtle
# create turtle object
t = turtle.Turtle()

# draw the letter R
t.pensize(10)
t.pencolor('red')
t.right(90)
t.forward(100)
t.right(90)
t.forward(50)
t.right(90)
t.forward(50)
t.right(90)
t.forward(50)
t.right(135)
t.forward(71)
t.right(45)
t.forward(50)

# hide turtle
t.hideturtle()

# keep the window open
turtle.done()



